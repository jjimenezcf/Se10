    ON [CALLEJERO].[PAIS]([ID_CREADOR] ASC);


GO
CREATE NONCLUSTERED INDEX [I_PAIS_ID_MODIFICADOR]
    ON [CALLEJERO].[PAIS]([ID_MODIFICADOR] ASC);


GO
CREATE NONCLUSTERED INDEX [I_PAIS_NOMBRE]
    ON [CALLEJERO].[PAIS]([NOMBRE] ASC);

