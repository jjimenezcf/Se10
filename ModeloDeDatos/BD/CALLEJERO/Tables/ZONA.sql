CREATE TABLE [CALLEJERO].[ZONA] (
    [ID]             INT           IDENTITY (1, 1) NOT NULL,
    [ID_MUNICIPIO]   INT           NOT NULL,
    [NOMBRE]         VARCHAR (250) NOT NULL,
    [FECCRE]         DATETIME2 (7) NOT NULL,
    [ID_CREADOR]     INT           NOT NULL,
    [FECMOD]         DATETIME2 (7) NULL,
    [ID_MODIFICADOR] INT           NULL,
    CONSTRAINT [PK_ZONA] PRIMARY KEY CLUSTERED ([ID] ASC),
    CONSTRAINT [FK_ZONA_ID_CREADOR] FOREIGN KEY ([ID_CREADOR]) REFERENCES [ENTORNO].[USUARIO] ([ID]),
    CONSTRAINT [FK_ZONA_ID_MODIFICADOR] FOREIGN KEY ([ID_MODIFICADOR]) REFERENCES [ENTORNO].[USUARIO] ([ID]),
    CONSTRAINT [FK_ZONA_ID_MUNICIPIO] FOREIGN KEY ([ID_MUNICIPIO]) REFERENCES [CALLEJERO].[MUNICIPIO] ([ID]),
    CONSTRAINT [AK_ZONA_ID_MUNICIPIO_NOMBRE] UNIQUE NONCLUSTERED ([ID_MUNICIPIO] ASC, [NOMBRE] ASC)
);


GO
CREATE NONCLUSTERED INDEX [I_ZONA_ID_MUNICIPIO]
    ON [CALLEJERO].[ZONA]([ID_MUNICIPIO] ASC);


GO
CREATE NONCLUSTERED INDEX [I_ZONA_ID_CREADOR]
    ON [CALLEJERO].[ZONA]([ID_CREADOR] ASC);


GO
CREATE NONCLUSTERED INDEX [I_ZONA_ID_MODIFICADOR]
    ON [CALLEJERO].[ZONA]([ID_MODIFICADOR] ASC);


GO
CREATE NONCLUSTERED INDEX [I_ZONA_NOMBRE]
    ON [CALLEJERO].[ZONA]([NOMBRE] ASC);

