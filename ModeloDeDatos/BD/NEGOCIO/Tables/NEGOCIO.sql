    ON [NEGOCIO].[NEGOCIO]([ID_ADM] ASC);


GO
CREATE NONCLUSTERED INDEX [I_NEGOCIO_ID_CONSULTOR]
    ON [NEGOCIO].[NEGOCIO]([ID_CONSULTOR] ASC);


GO
CREATE NONCLUSTERED INDEX [I_NEGOCIO_ID_GESTOR]
    ON [NEGOCIO].[NEGOCIO]([ID_GESTOR] ASC);


GO
CREATE UNIQUE NONCLUSTERED INDEX [I_NEGOCIO_NOMBRE]
    ON [NEGOCIO].[NEGOCIO]([NOMBRE] ASC);

