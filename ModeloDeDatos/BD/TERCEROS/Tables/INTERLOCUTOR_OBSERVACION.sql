CREATE TABLE [TERCEROS].[INTERLOCUTOR_OBSERVACION] (
    [ID]          INT            IDENTITY (1, 1) NOT NULL,
    [NOMBRE]      VARCHAR (250)  NOT NULL,
    [ID_ELEMENTO] INT            NOT NULL,
    [DESCRIPCION] VARCHAR (2000) NULL,
    [ID_CREADOR]  INT            NOT NULL,
    [CREADO_EL]   DATETIME2 (7)  NOT NULL,
    [CREADOR]     AS             ([ENTORNO].[CC_USUARIO_EXPRESION]([ID_CREADOR])),
    [ELEMENTO]    AS             ([TERCEROS].[CC_INTERLOCUTOR_NOMBRE]([ID_ELEMENTO])),
    CONSTRAINT [PK_INTERLOCUTOR_OBSERVACION] PRIMARY KEY CLUSTERED ([ID] ASC),
    CONSTRAINT [FK_INTERLOCUTOR_OBSERVACION_ID_CREADOR] FOREIGN KEY ([ID_CREADOR]) REFERENCES [ENTORNO].[USUARIO] ([ID]),
    CONSTRAINT [FK_INTERLOCUTOR_OBSERVACION_ID_ELEMENTO] FOREIGN KEY ([ID_ELEMENTO]) REFERENCES [TERCEROS].[INTERLOCUTOR] ([ID])
);


GO
CREATE NONCLUSTERED INDEX [I_INTERLOCUTOR_OBSERVACION_ID_CREADOR]
    ON [TERCEROS].[INTERLOCUTOR_OBSERVACION]([ID_CREADOR] ASC);


GO
CREATE NONCLUSTERED INDEX [I_INTERLOCUTOR_OBSERVACION_ID_ELEMENTO]
    ON [TERCEROS].[INTERLOCUTOR_OBSERVACION]([ID_ELEMENTO] ASC);

