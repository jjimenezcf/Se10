    ON [TERCEROS].[SOCIEDAD_DIRECCION]([CALIFICADOR] ASC, [ID_ELEMENTO] ASC, [ID_PAIS] ASC, [ID_PROVINCIA] ASC, [ID_MUNICIPIO] ASC, [ID_CALLE] ASC, [NUMERO] ASC, [PUERTA] ASC, [ESCALERA] ASC, [PISO] ASC) WHERE ([NUMERO] IS NOT NULL AND [PUERTA] IS NOT NULL AND [ESCALERA] IS NOT NULL AND [PISO] IS NOT NULL);


