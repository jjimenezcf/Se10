    CONSTRAINT [AK_PERMISO_POR_TIPO] UNIQUE NONCLUSTERED ([ID_NEGOCIO] ASC, [ID_TIPO] ASC, [ID_USUARIO] ASC, [ID_PERMISO] ASC)
