CREATE TABLE [TAREA].[TAREA_TIPO] (
    [ID]                 INT           IDENTITY (1, 1) NOT NULL,
    [ID_INTERVENTOR]     INT           NOT NULL,
    [CLASE_TAREA]        VARCHAR (15)  NOT NULL,
    [ID_TIPO_ARCHIVADOR] INT           NULL,
    [NOMBRE]             VARCHAR (250) NOT NULL,
    [ID_PADRE]           INT           NULL,
